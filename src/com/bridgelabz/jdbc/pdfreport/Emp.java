package com.bridgelabz.jdbc.pdfreport;

public class Emp {
	
  private int eno;
  private String name;
  private int eid;
  
  
public int getEno() {
	return eno;
}
public void setEno(int eno) {
	this.eno = eno;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public int getEid() {
	return eid;
}
public void setEid(int eid) {
	this.eid = eid;
}

}
