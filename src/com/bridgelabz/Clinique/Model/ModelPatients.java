package com.bridgelabz.Clinique.Model;

public class ModelPatients {
	
String PatientsName;
long PatientsId;
long PatientsMobail;
long Age;

public String getPatientsName() {
	return PatientsName;
}
public void setPatientsName(String patientsName) {
	PatientsName = patientsName;
}
public long getPatientsId() {
	return PatientsId;
}
public void setPatientsId(long patientsId) {
	PatientsId = patientsId;
}
public long getPatientsMobail() {
	return PatientsMobail;
}
public void setPatientsMobail(long patientsMobail) {
	PatientsMobail = patientsMobail;
}
public long getAge() {
	return Age;
}
public void setAge(long age) {
	Age = age;
}

}
